"""
rl_land_mask.py -- 북극 항로용 육지 충돌 감지 (간소화 버전)

주요 북극권 육지 영역을 다각형으로 근사하여
선박 위치가 육지인지 바다인지 판별한다.
"""
from __future__ import annotations

# ── 주요 북극권 육지 경계 박스 (lat_min, lat_max, lon_min, lon_max) ──
# 경도 범위: -180 ~ 180
_LAND_BOXES: list[tuple[float, float, float, float]] = [
    # 그린란드
    (59.0, 83.5, -73.0, -18.0),
    # 아이슬란드
    (63.0, 67.0, -25.0, -13.0),
    # 스발바르 제도 (Svalbard)
    (74.0, 81.0, 10.0, 33.0),
    # 노바야 제믈랴 (Novaya Zemlya)
    (70.0, 77.5, 51.0, 68.5),
    # 세베르나야 제믈랴 (Severnaya Zemlya)
    (78.0, 81.5, 91.0, 107.0),
    # 프란츠 요제프 제도
    (79.5, 82.0, 44.0, 65.0),
    # 뉴시베리아 제도
    (73.0, 76.5, 136.0, 159.0),
    # 브란겔 섬
    (70.5, 71.5, -179.0, -178.0),
    (70.5, 71.5, 178.5, 180.0),
    # 캐나다 북극 제도 (배핀 섬 등)
    (62.0, 84.0, -120.0, -61.0),
    # 알래스카 북부
    (60.0, 71.5, -168.0, -141.0),
    # 스칸디나비아 반도 북부 (노르웨이)
    (57.0, 71.5, 4.0, 31.0),
    # 러시아 시베리아 북부 해안선 근처 주요 반도
    # 타이미르 반도
    (71.0, 77.5, 88.0, 108.0),
    # 야말 반도
    (68.0, 73.5, 65.0, 74.0),
    # 축치 반도
    (64.0, 68.0, 170.0, 180.0),
    # 캄차카 반도
    (51.0, 60.0, 160.0, 163.0),
    # 홋카이도 북부
    (43.0, 45.8, 141.0, 145.5),
    # 사할린
    (45.8, 54.5, 141.5, 143.5),
    # 한국 (부산 출발지 육지 충돌 방지용 제외 - 넓은 바다 쪽만)
]


class LandMask:
    """간소화된 육지 마스크: 경계 박스 기반 육지 판별."""

    def is_land(self, lat: float, lon: float) -> bool:
        """주어진 좌표가 육지이면 True, 바다이면 False."""
        # 경도 정규화 (-180 ~ 180)
        lon_n = ((lon + 180.0) % 360.0) - 180.0

        for lat_min, lat_max, lon_min, lon_max in _LAND_BOXES:
            if lat_min <= lat <= lat_max and lon_min <= lon_n <= lon_max:
                return True
        return False
